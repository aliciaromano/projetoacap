  <h1>Quem Somos?</h1>
  <p style="text-indent: 3em;">
    "Na década de 80, por interesse de um grupo de jovens, despertado por Raimundo Anedino técnico da EMATER-RN da nossa região, surgiu a ideia de criar um grupo de apicultores no nosso município Cerro Corá/RN. Nos primeiros passos alguns jovens foram capacitados e em seguida vinheram a adquirir alguns colmeias, e assim foi dado o início da atividade apícula em nossa comunidade. Sendo assim a atividade foi tendo desenvoltura, demonstrando interesse de outros, e sendo engajados alguns interessados à praticar esse feito tão preciso para a natureza. Já por necessidade nas décadas de 90, através do PAP (Programa de Apoio a Agricultura Familiar) foi adquirido o primeiro recurso para a compra de colmeias e alguns equipamentos, que foram distribuídos para esse tão importante grupo.</p>
     <p style="text-indent: 3em;">Finalmente no ano de 2004, fomos constituídos juridicamente, criando a nossa associação com a sigla de ACAP (Associação Cerrocoraense de Apicultores), em pouco tempo devido ao nosso grande avanço, conseguimos pleitear novamente, um recurso do Banco Mundial através do Bando do Estado, por nome de: PDS (Programa de Desenvolvimento Solidário), recurso esse que foi aplicado na construção de uma unidade de beneficiamento com todos os equipamentos, e em seguida com complemento de verba conseguimos a nossa certificação mediante ao IDIARN, órgão este que nos rege qualidade de sanidade do nosso produto. Com a aplicação total e prestação de contas aprovadas, desde recursos, conseguimos através do programa RN Sustentável, verba esta também do Banco Mundial, pleitear um valor no teto de R$ 430.000,00 para uma reforma de adequação para o registro de inspeção federal (SIF), que a partir desse momento poderemos produzir e comercializar nossos produtos em todo o território nacional, chegando até a exportação da nossa matéria. </p>
    <p style="text-indent: 3em;">Por último tivemos uma conquista muito gratificante, que foi o registro da nossa própria marca, a ACAPMEL. Este feito só nos vem a nos motivarmos a cada vez mais, fortalecer na atividade que tem grande êxodo na agricultura familiar, como também uma grande contribuição em todo o nosso ecossistema. Convido a todos para conhecerem de perto, a nossa atividade com também os nosso produtos." <strong><i>-José Luiz, presidente da ACAP.</i></strong>
  </p>
